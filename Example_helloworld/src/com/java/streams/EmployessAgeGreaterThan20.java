package com.java.streams;

public class EmployessAgeGreaterThan20 {

	String name;
	int age;

	EmployessAgeGreaterThan20(String name, int age) {
		this.name = name;
		this.age = age;
	}

	@Override
	public String toString() {
		return "EmployessAgeGreaterThan20 [name=" + name + ", age=" + age + "]";
	}

	
}
